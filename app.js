const calculator = {
    add : function(a, b) {
        alert(a + b);
    },
    minus : function(a, b) {
        alert(a - b);
    },
    divide : function(a, b) {
        alert(a / b);
    },
    powerof : function(a, b) {
        alert(a ** b);
    }
};
calculator.add(1, 2);
calculator.minus(1, 2);
calculator.divide(1, 2);
calculator.powerof(2, 2);